package dat.src;


public class aritmatika {
    public static void main(String[] args) {
    //Operasi aritmatika
    int var1 = 5;
    int var2 =3;
    int hasil;
    //1.Penumlahan
    hasil=var1+var2;
    System.out.printf("hasil dari %d + %d = %d",var1,var2,hasil); 
    //2.perkalian
    hasil=var1*var2;
    System.out.printf("\nhasil dari %d * %d = %d\n",var1,var2,hasil);
    //3.Pengurangan
    hasil=var1-var2;
    System.out.println("Hasil dari "+var1+ "-" +var2+ "="+hasil);
    }
  
}
